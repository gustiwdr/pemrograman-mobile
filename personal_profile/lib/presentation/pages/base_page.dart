          //   label: 'Blogs',
          // ),
          // BottomNavigationBarItem(
          //   icon: Icon(Icons.phone),
          //   label: 'Contacts',
          // ),
          BottomNavigationBarItem(
            icon: Icon(Icons.settings),
            label: 'Settings',
          ),
        ],
        currentIndex: selectedIndex,
        selectedItemColor: Colors.indigo,
        onTap: _controller.onBottomNavTapped,
      ),
    );
  }
}
